    private void fillForm() throws Exception {
        ArrayList<Product> products = (ArrayList<Product>) Controller.getInstance().getAllProducts();
        ProductTableModel ptm = new ProductTableModel(products);
        tblProducts.setModel(ptm);

        
       
        Order order = new Order();
        
        tblOrderItems.setModel(new OrderTableModel(order));
    }

    private void fillGuest() throws Exception {
        ArrayList<Guest> guests = Controller.getInstance().getAllGuest();
        for(Guest g:guests){
            cmbGuest.addItem(g);
        }
    }
