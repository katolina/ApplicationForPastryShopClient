    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        int row = tblProducts.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select row");
            return;
        }

        String quantity = txtQuantity.getText();
        if (quantity.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a quantity");
            return;
        }
        int quanty = Integer.parseInt(quantity);

        
        ProductTableModel ptm = (ProductTableModel) tblProducts.getModel();
        int idSelectedProduct = (int) ptm.getValueAt(row, 0);
        Product selectedProduct = ptm.getProduct(idSelectedProduct);

        if (quanty > selectedProduct.getQuantity()) {
            JOptionPane.showMessageDialog(this, "The ordered quantity is greater than the quantity in stock");
            return;
        }
        if(quanty < 0){
            JOptionPane.showMessageDialog(this, "Quantity can not be bellow 0");
            return;
        }

        String date = txtDateOrder.getText();
        if (date.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Date is empty");
            return;
        }
        try {

            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            Date dateOrder = sdf.parse(date);

            TableModel tm = tblOrderItems.getModel();
            OrderTableModel otm = (OrderTableModel) tm;
            
            for(OrderItem item:otm.getOrder().getOrderItems()){
            if(item.getProduct() == selectedProduct){
                JOptionPane.showMessageDialog(this, "You can not add the same product");
                return;
            }
        }
            otm.addProduct(selectedProduct, dateOrder, quanty);

        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Date is in format dd.MM.yyyy.");
        }


    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteOrderItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteOrderItemActionPerformed
        int row = tblOrderItems.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please, select row");
            return;
        }

        TableModel tm = tblOrderItems.getModel();
        OrderTableModel otm = (OrderTableModel) tm;
        otm.deleteOrderItem(row);
    }//GEN-LAST:event_btnDeleteOrderItemActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        try {
            String date = txtDateOrder.getText();
            SimpleDateFormat sdf = new SimpleDateFormat("dd.MM.yyyy.");
            Date datedate = sdf.parse(date);
            Guest guest = (Guest) cmbGuest.getSelectedItem();
            
            Order order = ((OrderTableModel) tblOrderItems.getModel()).getOrder();
            order.setOrderDate(datedate);
            order.setGuest(guest);
            Controller.getInstance().saveOrder(order);
           
            JOptionPane.showMessageDialog(this, "Order is saved!");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,"Order isn't saved!" );
        }


    }//GEN-LAST:event_btnSaveActionPerformed


