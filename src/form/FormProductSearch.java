    private void fillForm() {

        try {
           
            List<Product> products = Controller.getInstance().getAllProducts();
            TableModel tm = new ProductTableModel(products);
            tblProductSearch.setModel(tm);
            MeasurementUnit[] measurementUnits = new MeasurementUnit[]{MeasurementUnit.KG, MeasurementUnit.L, MeasurementUnit.PCS};
            JComboBox comboMeasurementUnits = new JComboBox(new DefaultComboBoxModel(measurementUnits));
            TableColumn tc = tblProductSearch.getColumnModel().getColumn(3);
            tc.setCellEditor(new DefaultCellEditor(comboMeasurementUnits));
            
           
    }   catch (Exception ex) {
            Logger.getLogger(FormProductSearch.class.getName()).log(Level.SEVERE, null, ex);
        }
}

  

    void delete(Product product) {
        ProductTableModel ptm = (ProductTableModel) tblProductSearch.getModel();
        ptm.delete(product);
        
    }

    void addProduct(Product product) {
        ProductTableModel ptm = (ProductTableModel) tblProductSearch.getModel();
        ptm.addProduct(product);
    }
    
