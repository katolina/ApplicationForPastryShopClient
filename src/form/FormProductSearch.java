    private void btnDetailsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailsActionPerformed
        int row = tblProductSearch.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select row");
            return;
        } 
            ProductTableModel ptm = (ProductTableModel) tblProductSearch.getModel();
            int idSelectedProduct = (int) ptm.getValueAt(row, 0);
            Product product = ptm.getProduct(idSelectedProduct);
            Session.getInstance().setFormMode(FormMode.VIEW);
            FormNewProduct form = new FormNewProduct(null, true, FormMode.VIEW);
            form.formProductSearch = this;
            form.setProduct(product);
            form.setVisible(true);
        

    }//GEN-LAST:event_btnDetailsActionPerformed


